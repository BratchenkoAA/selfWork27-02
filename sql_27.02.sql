-- Создаем схему (если не существует)
CREATE SCHEMA IF NOT EXISTS project_management;

-- Переключаемся на схему
SET search_path TO project_management;

-- Удаляем старые таблицы, если они есть
DROP TABLE IF EXISTS assignments CASCADE;
DROP TABLE IF EXISTS tasks CASCADE;
DROP TABLE IF EXISTS projects CASCADE;
DROP TABLE IF EXISTS employees CASCADE;

-- Таблица 1: СОТРУДНИКИ
CREATE TABLE employees (
    employee_id SERIAL PRIMARY KEY,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    email VARCHAR(100) UNIQUE,
    role VARCHAR(50)
);

-- Таблица 2: ПРОЕКТЫ
CREATE TABLE projects (
    project_id SERIAL PRIMARY KEY,
    project_name VARCHAR(100) NOT NULL,
    start_date DATE,
    end_date DATE,
    status VARCHAR(20) DEFAULT 'Планируется'
);

-- Таблица 3: ЗАДАЧИ
CREATE TABLE tasks (
    task_id SERIAL PRIMARY KEY,
    project_id INT REFERENCES projects(project_id) ON DELETE CASCADE,
    title VARCHAR(150) NOT NULL,
    due_date DATE,
    priority INT
);

-- Таблица 4: НАЗНАЧЕНИЯ
CREATE TABLE assignments (
    assignment_id SERIAL PRIMARY KEY,
    task_id INT REFERENCES tasks(task_id) ON DELETE CASCADE,
    employee_id INT REFERENCES employees(employee_id) ON DELETE CASCADE,
    assigned_date DATE DEFAULT CURRENT_DATE
);

